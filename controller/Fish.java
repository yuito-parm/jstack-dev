package controller;


public class Fish {
    public String fishName;
    public int price;
    public String feature;
    public String status;
    public int stock;

    public Fish(String fishName, int price, String feature, String status, int stock) {
        this.fishName = fishName;
        this.price = price;
        this.feature = feature;
        this.status = status;
        this.stock = stock;
    }
}
